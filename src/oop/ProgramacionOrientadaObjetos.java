// Definición de una clase que representa un ejemplo de OOP
public class ProgramacionOrientadaObjetos {

    // Método principal para ejecutar el programa
    public static void main(String[] args) {
        // Imprimir una introducción a la programación orientada a objetos
        System.out.println("Programación Orientada a Objetos (OOP) en Java");

        // Explicar la OOP y sus características
        System.out.println("La Programación Orientada a Objetos (OOP) es un paradigma de programación que se basa en el uso de objetos.");
        System.out.println("Sus características principales son:");

        // Características de la OOP
        System.out.println("1. Encapsulamiento: Permite ocultar la implementación interna de un objeto y exponer solo lo necesario.");
        System.out.println("2. Herencia: Permite la creación de nuevas clases basadas en clases existentes, promoviendo la reutilización de código.");
        System.out.println("3. Polimorfismo: Permite a un objeto tomar múltiples formas, facilitando el tratamiento uniforme de objetos de clases diferentes.");
        System.out.println("4. Abstracción: Simplifica un objeto complejo mediante la definición de una interfaz clara y la ocultación de los detalles internos.");

        // Fin del programa
        System.out.println("¡Gracias por aprender sobre Programación Orientada a Objetos en Java!");
    }
}
